-module(protocol).

%% API
-export([encode/1, decode/1, broadcast_port/0]).

%%====================================================================
%% API
%%====================================================================
%%--------------------------------------------------------------------
%% Function: broadcast_port()
%% Description:
%%--------------------------------------------------------------------
broadcast_port() -> 3141.

encode(Terms) ->
    io_lib:format("~p.", [Terms]).
    
decode(Data) ->
    {ok, Tokens, _} = erl_scan:string(Data),
    {ok, Term} = erl_parse:parse_term(Tokens),
    Term.
    
